async def add_memory(chunk_data: Dict[str, Any], dataset_name: str = "kyro_memories"):
    """
    Add a chunk of memory to Cognee.
    Cognee Cloud manages the graph generation pipeline remotely.
    """
    text = chunk_data.get("text", "")
    if not text:
        return
        
    try:
        # 1. Upload data to Cloud
        await cognee.add(text, dataset_name=dataset_name)
        # 2. Trigger graph processing on Cloud
        await cognee.cognify(datasets=dataset_name)
        logger.info(f"Successfully added memory to Cognee dataset: {dataset_name}")
    except Exception as e:
        logger.error(f"Error adding memory to Cognee: {e}")
        raise

async def search_memories(query: str, dataset_name: str = "kyro_memories", limit: int = 5) -> List[Dict[str, Any]]:
    """
    Search memories using Cognee Cloud.
    """
    try:
        # Search directly returns insights generated by the Cloud's LLM pipeline
        results = await cognee.search(query_text=query, query_type=SearchType.RAG_COMPLETION, datasets=[dataset_name])
        
        final_results = []
        for item in results:
            # Parse the response dynamically depending on what the SDK returns
            if isinstance(item, dict):
                text = item.get("text", str(item))
                mem_id = item.get("id", str(item))
                score = item.get("score", 1.0)
            else:
                text = str(item)
                mem_id = str(item)
                score = 1.0
                
            weight = MEMORY_WEIGHTS.get(mem_id, 1.0)
            final_results.append({
                "text": text,
                "weight": weight,
                "score": score * weight,
                "id": mem_id
            })
            
        return sorted(final_results, key=lambda x: x["score"], reverse=True)[:limit]
    except Exception as e:
        logger.error(f"Error searching memories in Cognee: {e}")
        return []

