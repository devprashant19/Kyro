async def setup_cognee():
    """Initialize cognee and connect to the cloud tenant."""
    api_key = os.environ.get("COGNEE_API_KEY")
    base_url = os.environ.get("COGNEE_BASE_URL")
    tenant_id = os.environ.get("COGNEE_TENANT_ID")
    
    if api_key and base_url:
        try:
            logger.info(f"Connecting Cognee SDK to Cloud Tenant at {base_url}...")
            # cognee.serve points the local SDK to the remote AWS tenant
            await cognee.serve(url=base_url, api_key=api_key)
            logger.info("Successfully connected Cognee SDK to Cloud.")
        except Exception as e:
            logger.error(f"Failed to connect to Cognee Cloud: {e}")
            raise
